۳. مکتب نئوکلاسیک یا روابط انسانی (دهه ۱۹۳۰ تا ۱۹۵۰)
مکتب کلاسیک به دلیل نگاه مکانیکی به انسان و نادیده گرفتن ابعاد روانی و اجتماعی کارکنان مورد انتقاد قرار گرفت. مکتب نئوکلاسیک در واکنش به این ضعف‌ها ظهور کرد و "انسان" را در کانون توجه قرار داد.

چهره کلیدی: التون مایو (Elton Mayo) و مطالعات هاثورن (Hawthorne Studies).

نقطه عطف: مطالعات هاثورن که در ابتدا برای بررسی تأثیر شرایط فیزیکی (مانند نور) بر بهره‌وری انجام شد، به نتایج غیرمنتظره‌ای رسید. محققان دریافتند که توجه به کارکنان، احساس تعلق به گروه و روابط اجتماعی، تأثیری به مراتب بیشتر از عوامل فیزیکی بر افزایش بهره‌وری دارد.

ایده اصلی: رضایت شغلی و روحیه کارکنان، نقش کلیدی در عملکرد آنها دارد. سازمان یک سیستم اجتماعی است، نه فقط یک ساختار فنی.

نظریه‌پردازان کلیدی دیگر:

آبراهام مازلو (Abraham Maslow): با ارائه هرم سلسله‌مراتب نیازها، نشان داد که انسان‌ها نیازهای مختلفی (از فیزیولوژیک تا خودشکوفایی) دارند که باید برای ایجاد انگیزه در آنها، این نیازها برآورده شوند.

داگلاس مک‌گرگور (Douglas McGregor): با ارائه نظریه X و Y، دو دیدگاه متضاد مدیران به کارکنان را توصیف کرد. مدیران نظریه X کارکنان را تنبل و بی‌انگیزه می‌دانند (دیدگاه کلاسیک)، در حالی که مدیران نظریه Y آنها را مسئولیت‌پذیر، خلاق و باانگیزه می‌پندارند (دیدگاه روابط انسانی).


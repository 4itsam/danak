۱. ریشه‌های باستانی مدیریت (پیش از سال ۱۷۷۶)
اگرچه مدیریت به عنوان یک رشته علمی پدیده‌ای مدرن است، اما اصول و شیوه‌های آن از هزاران سال پیش وجود داشته است. تمدن‌های بزرگ باستانی برای ساخت سازه‌های عظیم، اداره امپراتوری‌های گسترده و سازماندهی جوامع پیچیده، ناگزیر به استفاده از اصول مدیریت بودند.

سومریان (حدود ۳۰۰۰ سال پیش از میلاد): سومریان در بین‌النهرین، سیستم‌های دقیقی برای ثبت و ضبط معاملات تجاری، مالیات و دارایی‌های معابد ابداع کردند. لوح‌های گلی به جا مانده، نشان‌دهنده وجود نظام‌های کنترل و پاسخگویی است که توسط کاهنان اجرا می‌شد.

مصر باستان (حدود ۲۹۰۰ سال پیش از میلاد): ساخت اهرام ثلاثه، نمونه‌ای شگفت‌انگیز از مدیریت پروژه در دنیای باستان است. این پروژه‌ها نیازمند برنامه‌ریزی دقیق، سازماندهی نیروی کار (بیش از ۱۰۰ هزار نفر)، تخصیص منابع، و کنترل کیفیت در مقیاسی غیرقابل تصور بود.

ایران باستان (دوره هخامنشیان): امپراتوری هخامنشی با ایجاد نظام ساتراپی (ایالتی)، یک ساختار مدیریتی غیرمتمرکز اما هماهنگ را پیاده‌سازی کرد. استفاده از چاپارخانه‌ها به عنوان یک شبکه ارتباطی سریع، و سیاست‌های مبتنی بر تسامح و احترام به فرهنگ‌های محلی، نمونه‌هایی از مدیریت استراتژیک و هوشمندانه در آن دوران است.

۲. مکتب کلاسیک مدیریت (اواخر قرن ۱۹ تا اوایل قرن ۲۰)
با وقوع انقلاب صنعتی و ظهور کارخانه‌های بزرگ، نیاز به یک رویکرد سیستماتیک برای افزایش کارایی و بهره‌وری به شدت احساس شد. مکتب کلاسیک اولین تلاش علمی برای تدوین اصول مدیریت بود و بر ساختار، کارایی و کنترل تمرکز داشت.

این مکتب خود به سه شاخه اصلی تقسیم می‌شود:

الف) مدیریت علمی (Scientific Management)
چهره کلیدی: فردریک وینسلو تیلور (Frederick W. Taylor)، که به "پدر مدیریت علمی" مشهور است.

زمینه پیدایش: تیلور در کارخانه‌ها با پدیده‌هایی مانند اتلاف وقت، کم‌کاری کارگران (Soldiering) و عدم وجود استاندارد برای انجام کارها مواجه بود.

ایده اصلی: او معتقد بود که می‌توان با مطالعه علمی حرکات و زمان‌سنجی (Time and Motion Study)، "بهترین و تنها راه" (One Best Way) برای انجام هر کاری را پیدا کرد.

اصول کلیدی:

جایگزینی روش‌های سنتی با روش‌های علمی: تحلیل وظایف به صورت علمی.

انتخاب و آموزش علمی کارکنان: انتخاب افرادی که از نظر فیزیکی و ذهنی برای کار مناسب هستند و آموزش علمی به آنها.

همکاری صمیمانه مدیران و کارکنان: حصول اطمینان از اینکه کارها طبق اصول علمی انجام می‌شود.

تقسیم منصفانه کار و مسئولیت: مدیریت مسئول برنامه‌ریزی و طراحی کار است و کارگران مسئول اجرای آن.

ب) مدیریت اداری (Administrative Management)
چهره کلیدی: هنری فایول (Henri Fayol)، یک مهندس و مدیر فرانسوی.

ایده اصلی: تمرکز فایول برخلاف تیلور که بر کار کارگران متمرکز بود، بر روی وظایف مدیران و کل سازمان بود. او تلاش کرد اصولی جهان‌شمول برای مدیریت ارائه دهد.

اصول کلیدی: فایول ۱۴ اصل مدیریتی معروف را معرفی کرد که برخی از مهم‌ترین آنها عبارتند از: تقسیم کار، اختیار و مسئولیت، انضباط، وحدت فرماندهی، وحدت جهت، و تمرکزگرایی.

وظایف مدیر: او همچنین برای اولین بار وظایف اصلی مدیران را در پنج حوزه تعریف کرد: برنامه‌ریزی، سازماندهی، فرماندهی، هماهنگی و کنترل.

ج) بوروکراسی (Bureaucracy)
چهره کلیدی: ماکس وبر (Max Weber)، جامعه‌شناس آلمانی.

زمینه پیدایش: وبر به دنبال یافتن یک ساختار سازمانی ایده‌آل بود که مبتنی بر منطق و عقلانیت باشد و از خویشاوندسالاری و تصمیمات شخصی جلوگیری کند.

ایده اصلی: بوروکراسی یک ساختار سازمانی رسمی، منطقی و غیرشخصی است که بر اساس قوانین و مقررات واضح عمل می‌کند.


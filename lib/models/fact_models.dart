۳. مکتب نئوکلاسیک یا روابط انسانی (دهه ۱۹۳۰ تا ۱۹۵۰)
مکتب کلاسیک به دلیل نگاه مکانیکی به انسان و نادیده گرفتن ابعاد روانی و اجتماعی کارکنان مورد انتقاد قرار گرفت. مکتب نئوکلاسیک در واکنش به این ضعف‌ها ظهور کرد و "انسان" را در کانون توجه قرار داد.

چهره کلیدی: التون مایو (Elton Mayo) و مطالعات هاثورن (Hawthorne Studies).

نقطه عطف: مطالعات هاثورن که در ابتدا برای بررسی تأثیر شرایط فیزیکی (مانند نور) بر بهره‌وری انجام شد، به نتایج غیرمنتظره‌ای رسید. محققان دریافتند که توجه به کارکنان، احساس تعلق به گروه و روابط اجتماعی، تأثیری به مراتب بیشتر از عوامل فیزیکی بر افزایش بهره‌وری دارد.

ایده اصلی: رضایت شغلی و روحیه کارکنان، نقش کلیدی در عملکرد آنها دارد. سازمان یک سیستم اجتماعی است، نه فقط یک ساختار فنی.

نظریه‌پردازان کلیدی دیگر:

آبراهام مازلو (Abraham Maslow): با ارائه هرم سلسله‌مراتب نیازها، نشان داد که انسان‌ها نیازهای مختلفی (از فیزیولوژیک تا خودشکوفایی) دارند که باید برای ایجاد انگیزه در آنها، این نیازها برآورده شوند.

داگلاس مک‌گرگور (Douglas McGregor): با ارائه نظریه X و Y، دو دیدگاه متضاد مدیران به کارکنان را توصیف کرد. مدیران نظریه X کارکنان را تنبل و بی‌انگیزه می‌دانند (دیدگاه کلاسیک)، در حالی که مدیران نظریه Y آنها را مسئولیت‌پذیر، خلاق و باانگیزه می‌پندارند (دیدگاه روابط انسانی).

۴. مکتب کمی یا علم مدیریت (دهه ۱۹۴۰ به بعد)
این مکتب که ریشه در تحقیقات عملیاتی جنگ جهانی دوم دارد، بر استفاده از روش‌های ریاضی، آمار و مدل‌سازی کامپیوتری برای حل مسائل مدیریتی و تصمیم‌گیری تأکید می‌کند.

زمینه پیدایش: نیاز ارتش‌ها به تخصیص بهینه منابع کمیاب (مانند سرباز، تجهیزات و تدارکات) در طول جنگ.

ایده اصلی: می‌توان مسائل پیچیده مدیریتی مانند کنترل موجودی، زمان‌بندی تولید، و تخصیص منابع را به مدل‌های ریاضی تبدیل کرد و بهترین راه‌حل ممکن را به صورت علمی پیدا کرد.

حوزه‌های کاربرد: مدیریت تولید، مدیریت مالی، برنامه‌ریزی استراتژیک و لجستیک.

۵. رویکردهای نوین مدیریت (دهه ۱۹۶۰ به بعد)
نظریه‌های جدیدتر تلاش کردند تا دیدگاه‌های مکاتب قبلی را با هم ترکیب کرده و نگاهی جامع‌تر و واقع‌بینانه‌تر به سازمان ارائه دهند.

الف) رویکرد سیستمی (Systems Approach)
ایده اصلی: این رویکرد، سازمان را مانند یک موجود زنده، یک سیستم باز در نظر می‌گیرد که از اجزای به هم پیوسته (زیرسیستم‌ها) تشکیل شده و با محیط خود در تعامل است. سازمان، ورودی‌ها (منابع) را از محیط دریافت کرده، آنها را پردازش می‌کند و خروجی (کالا یا خدمات) را به محیط تحویل می‌دهد.

مفاهیم کلیدی:

هم‌افزایی (Synergy): کل سیستم، ارزشی بیشتر از مجموع اجزای خود تولید می‌کند (2+2=5).

آنتروپی (Entropy): تمایل سیستم‌های بسته به زوال و نابودی. سازمان‌ها برای بقا باید سیستم باز باشند و با محیط تعامل کنند.

نتیجه‌گیری مدیریتی: مدیران باید بدانند که تصمیمات آنها در یک بخش از سازمان، بر سایر بخش‌ها نیز تأثیر می‌گذارد و موفقیت سازمان در گرو هماهنگی همه اجزا و سازگاری با محیط است.

ب) رویکرد اقتضایی (Contingency Approach)
ایده اصلی: این رویکرد، ایده‌ی "بهترین و تنها راه" (که در مکتب کلاسیک مطرح بود) را به کلی رد می‌کند. بر اساس این دیدگاه، هیچ اصل مدیریتی واحدی وجود ندارد که در همه شرایط و برای همه سازمان‌ها بهترین باشد.

شعار اصلی: "بستگی دارد" (It Depends).

